package Day5_11042024;

public class T2_If_Else_Condition {
    public static void main(String[] args) {
        // Declares two integer variables
        int a = 1;
        int b = 2;

        // print only when a is less than b
        if(a < b){
            System.out.println("a is less than b");
        }
        else{
            System.out.println("a is either greater than or equal to b");
        }
    }
}
